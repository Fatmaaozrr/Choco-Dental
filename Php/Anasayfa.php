                        <br />
                        <p>İşlemi tamamlamak için genellikle iki diş hekimi randevusu alır, çünkü çoğu kronun bir diş laboratuarında yapılması gerekir.</p>
                        <br />
                        <p>Ancak bazı genel diş hekimleri kronları kendileri yapabilir ve bu da bir hastanın tek bir günde diş kronu yerleştirmesini mümkün kılar.</p>
                        <br />
                        <p>Kuronlar, dişleri tamamen kaplayarak onarır, bu da onları daha fazla enfeksiyon veya hasardan korur.</p>
