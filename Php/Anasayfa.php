                        <h1>Diş Dolguları</h1>
                        <p>Diş dolguları, çürük veya çürük olan dişleri tedavi etmek için kullanılır. Kırıkları olan dişleri onarmak için de kullanılabilirler.</p>
                        <br />
                        <p>Bir hasta diş ağrısından şikayet ettiğinde, diş hekiminin yaptığı ilk şey boşlukları ve çatlakları kontrol etmektir. Bunu dişi görsel olarak inceleyerek ve X ışınları kullanarak yaparlar. Diş hekimi, dişin doldurulması gerektiğini tespit ederse, o zaman dişi düzeltir.</p>
                        <br />
                        <p>Dişi uyuşturduktan sonra, diş hekimi iç dişteki çürük diş dokusunu çıkarmak için bir tedavi yöntemi uygulayacaktır. Daha sonra dişi bir diş dolgusu ile kapatmadan önce  su ile temizlerler.</p>
                        <br />
                        <p>Diş hekimi yardımı ile hasta tercih ettiği dolgu tipini seçecektir. Altın, gümüş, porselen veya diş reçinesi dolgularından seçim yapabilirler.</p>

                    </div>
